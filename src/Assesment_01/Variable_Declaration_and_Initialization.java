package Assesment_01;

public class Variable_Declaration_and_Initialization {
    public static void main(String[] args) {
        int a = 32;
        double b = 1234.456;
        boolean status = false;
        char grade = 'A';
        System.out.println("int data type and value of a is :"+a);
        System.out.println("double data type and value of b is :"+b);
        System.out.println("boolean data type and value of status is :"+status);
        System.out.println("char data type and value of grade is :"+grade);


    }
}
