package ex_02_Java_Basics;

public class Lab_04_Common_Math {
    public static void main(String[] args) {
        System.out.println(2+2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println(2-2);
        System.out.println("2+2");
        System.out.println(3/2);//output : 1
        System.out.println((float)3/2);//output : 1.5
        System.out.println((double)3/2);//output : 1.5
    }
}
